async def generate_fixes(scan_id: str) -> dict:
    """
    Generate tailored fix files based on a completed scan result.

    Args:
        scan_id: The scan ID to generate fixes for.

    Returns:
        Dict with files list, scan_id, and domain.

    Raises:
        ValueError: If scan not found or not completed.
    """
    scan = await get_scan(scan_id)
    if not scan:
        raise ValueError("Scan not found")

    if scan["status"] != "completed":
        raise ValueError(f"Scan is not completed (status: {scan['status']})")

    report = {}
    if scan["report_json"]:
        report = json.loads(scan["report_json"])

    domain = scan["domain"]
    checks = report.get("checks", [])

    files = [
        _generate_ai_txt(domain, checks),
        _generate_llms_txt(domain, checks, report),
        _generate_robots_txt_additions(domain, checks),
        _generate_ucp_json(domain, checks),
        _generate_product_schema(domain, checks),
        _generate_organization_schema(domain, checks),
        _generate_implementation_guide(domain, checks, report),
    ]

    return {
        "files": files,
        "scan_id": scan_id,
        "domain": domain,
    }
