def _extract_payment_methods(checks: list[dict]) -> list[str]:
    """Extract detected payment methods from scan checks."""
    for check in checks:
        if check.get("name") == "Payment Methods":
            msg = check.get("message", "")
            # Parse "Found: visa, mastercard, paypal" style messages
            match = re.search(r"(?:Found|found)[:\s]+(.+?)\.?$", msg)
            if match:
                return [m.strip() for m in match.group(1).split(",")]
            # Parse "on page (visa, mastercard)" style
            match = re.search(r"\(([^)]+)\)", msg)
            if match:
                return [m.strip() for m in match.group(1).split(",")]
    return []


def _extract_shipping_keywords(checks: list[dict]) -> list[str]:
    """Extract detected shipping keywords from scan checks."""
    for check in checks:
        if check.get("name") == "Shipping Information":
            msg = check.get("message", "")
            match = re.search(r"Keywords?[:\s]+(.+?)\.?$", msg)
            if match:
                return [m.strip() for m in match.group(1).split(",")]
    return []


def _extract_return_keywords(checks: list[dict]) -> list[str]:
    """Extract detected return policy keywords from scan checks."""
    for check in checks:
        if check.get("name") == "Return Policy":
            msg = check.get("message", "")
            match = re.search(r"Keywords?[:\s]+(.+?)\.?$", msg)
            if match:
                return [m.strip() for m in match.group(1).split(",")]
    return []


def _check_status(checks: list[dict], name: str) -> str:
    """Get the status of a specific check by name."""
    for check in checks:
        if check.get("name") == name:
            return check.get("status", "fail")
    return "fail"


def _check_has_pass(checks: list[dict], name: str) -> bool:
    return _check_status(checks, name) == "pass"


def _get_failed_and_warned(checks: list[dict]) -> list[dict]:
    """Get all checks that failed or warned."""
    return [c for c in checks if c.get("status") in ("fail", "warn")]


def _detect_platform(domain: str, checks: list[dict]) -> str:
    """Try to detect the e-commerce platform from check messages."""
    all_messages = " ".join(c.get("message", "") for c in checks).lower()
    all_snippets = " ".join(c.get("code_snippet", "") or "" for c in checks).lower()
    combined = all_messages + " " + all_snippets

    if "shopify" in combined or "myshopify" in domain:
        return "shopify"
    if "woocommerce" in combined or "wp-content" in combined:
        return "woocommerce"
    if "magento" in combined:
        return "magento"
    if "prestashop" in combined:
        return "prestashop"
    return "custom"


def _generate_ai_txt(domain: str, checks: list[dict]) -> dict:
    """Generate a tailored ai.txt file."""
    payment_methods = _extract_payment_methods(checks)
    shipping_keywords = _extract_shipping_keywords(checks)
    return_keywords = _extract_return_keywords(checks)
    has_guest_checkout = _check_has_pass(checks, "Guest Checkout")

    lines = [
        f"# ai.txt for {domain}",
        f"# Generated by AgentReady - {datetime.now(timezone.utc).strftime('%Y-%m-%d')}",
        "",
        f"Name: {domain}",
        f"Homepage: https://{domain}",
        "",
        "# Allowed actions for AI agents",
        "Allow: /products",
        "Allow: /collections",
        "Allow: /categories",
        "Allow: /shop",
        "Allow: /catalog",
        "Allow: /search",
        "",
        "# Disallowed paths",
        "Disallow: /checkout",
        "Disallow: /account",
        "Disallow: /admin",
        "Disallow: /cart/update",
        "",
        "# Capabilities",
    ]

    if has_guest_checkout:
        lines.append("Capability: guest-checkout")
    else:
        lines.append("# Capability: guest-checkout  (not yet detected - enable to improve score)")

    if payment_methods:
        lines.append(f"Payment-Methods: {', '.join(payment_methods)}")
    else:
        lines.append("# Payment-Methods: VISA, Mastercard, PayPal  (add your accepted methods)")

    if shipping_keywords:
        lines.append(f"Shipping: Available ({', '.join(shipping_keywords[:3])})")
    else:
        lines.append("# Shipping: Available  (add shipping details)")

    if return_keywords:
        lines.append(f"Returns: Available ({', '.join(return_keywords[:3])})")
    else:
        lines.append("# Returns: Available  (add return policy details)")

    lines.extend([
        "",
        "# Contact",
        f"# Contact-Email: support@{domain}  (replace with your actual email)",
        f"# Contact-URL: https://{domain}/contact",
        "",
        "# Agent instructions",
        "Instructions: AI agents are welcome to browse product listings, compare prices,",
        "  and assist users with purchasing decisions. Please respect rate limits.",
        "  Do not scrape all pages in bulk. Use structured data when available.",
    ])

    return {
        "name": "ai.txt",
        "content": "\n".join(lines) + "\n",
        "description": "AI agent instructions file - place at your domain root (e.g., https://yourdomain.com/ai.txt)",
    }


def _generate_llms_txt(domain: str, checks: list[dict], report: dict) -> dict:
    """Generate a tailored llms.txt file."""
    grade = report.get("grade", "?")
    score = report.get("total_score", 0)
    categories = report.get("categories", {})
    payment_methods = _extract_payment_methods(checks)

    # Build description based on what was found
    desc_parts = [f"Online shop at {domain}"]
    if payment_methods:
        desc_parts.append(f"accepting {', '.join(payment_methods[:3])}")
    if _check_has_pass(checks, "Guest Checkout"):
        desc_parts.append("with guest checkout available")

    description = " ".join(desc_parts) + "."

    lines = [
        f"# {domain}",
        "",
        f"> {description}",
        "",
        "## About",
        "",
        f"This is the llms.txt file for {domain}, providing context for large language models.",
        "",
        "## Shop Information",
        "",
        f"- Website: https://{domain}",
        f"- AI Readiness Score: {score}/100 (Grade: {grade})",
    ]

    if payment_methods:
        lines.append(f"- Payment Methods: {', '.join(payment_methods)}")
    else:
        lines.append("- Payment Methods: [Add your payment methods here]")

    has_shipping = _check_status(checks, "Shipping Information") != "fail"
    has_returns = _check_status(checks, "Return Policy") != "fail"

    if has_shipping:
        lines.append("- Shipping: Available (see website for details)")
    else:
        lines.append("- Shipping: [Add shipping information]")

    if has_returns:
        lines.append("- Returns: Available (see website for details)")
    else:
        lines.append("- Returns: [Add return policy information]")

    lines.extend([
        "",
        "## Key Pages",
        "",
        f"- Homepage: https://{domain}",
        f"- Products: https://{domain}/products",
        f"- Collections: https://{domain}/collections",
        f"- Contact: https://{domain}/contact",
        "",
        "## For AI Agents",
        "",
        "This shop welcomes AI agents for product discovery and comparison.",
        "Structured data (JSON-LD) is available on product pages.",
        f"See https://{domain}/ai.txt for detailed agent instructions.",
        f"UCP endpoint: https://{domain}/.well-known/ucp",
    ])

    return {
        "name": "llms.txt",
        "content": "\n".join(lines) + "\n",
        "description": "LLM context file - place at your domain root or /.well-known/llms.txt",
    }


